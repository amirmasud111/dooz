
const WIN_LINES = [
    [[0, 0], [0, 1], [0, 2]],
    [[1, 0], [1, 1], [1, 2]],
    [[2, 0], [2, 1], [2, 2]],

    [[0, 0], [1, 0], [2, 0]],
    [[0, 1], [1, 1], [2, 1]],
    [[0, 2], [1, 2], [2, 2]],

    [[0, 0], [1, 1], [2, 2]],
    [[0, 2], [1, 1], [2, 0]],
];

export function checkBoard(board: string[][], currentPlayer: string): number[][] | void {

    return WIN_LINES.find(WIN_LINE => WIN_LINE.every(e => board[e[0]][e[1]] == currentPlayer))

}